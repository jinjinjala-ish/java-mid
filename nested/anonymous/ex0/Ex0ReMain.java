package nested.anonymous.ex0;

public class Ex0ReMain {

    public static void print(String name) {
        System.out.println("프로그램 시작");
        System.out.println("헬로 " + name);
        System.out.println("프로그램 종료");
    }

    public static void main(String[] args) {
        print("java");
        print("Spring");
    }
}
